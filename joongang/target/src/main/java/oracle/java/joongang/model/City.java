package oracle.java.joongang.model;

public class City {
	private int b_city;
	private int m_city;
	private String city_name;
	public int getB_city() {
		return b_city;
	}
	public void setB_city(int b_city) {
		this.b_city = b_city;
	}
	public int getM_city() {
		return m_city;
	}
	public void setM_city(int m_city) {
		this.m_city = m_city;
	}
	public String getCity_name() {
		return city_name;
	}
	public void setCity_name(String city_name) { 
		this.city_name = city_name;
	} 
}
